# Ces lignes appellent la fonction Add trois fois avec des paires de nombres différents. 
# Lors de chaque appel, la fonction effectue l'addition des deux nombres et affiche le résultat.
def Add(a, b):
    resultat = a + b
    print(f"La somme de {a} et {b} est : {resultat}")

# Nous appelons la fonction 3 fois afin qu'elle puisse éxécuter chaque sommes dont nous lui avons réclamé de calculer.
Add(15,48)
Add(-6, -9)
Add(10, -8)
