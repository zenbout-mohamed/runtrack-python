# def My_print_hello():: Cette ligne définit une fonction nommée My_print_hello.
# Le mot-clé def est utilisé pour déclarer une fonction en Python, suivi du nom de la fonction, suivi de parenthèses vides ().
# Les parenthèses vides indiquent que la fonction ne prend aucun argument.
# print("Hello world"): À l'intérieur du corps de la fonction, il y a une seule instruction, qui est la fonction print().
# Cette fonction affiche le texte "Hello world" dans le terminal lorsqu'elle est appelée.
# Lorsque vous appelez la fonction en utilisant My_print_hello(),
# Le programme exécute le code à l'intérieur de la fonction, ce qui dans ce cas précis imprime "Hello world" dans le terminal.

def My_print_hello():
    print("Hello world")

# Puis nous faisons appel à la fonction comme ceci:
My_print_hello()
