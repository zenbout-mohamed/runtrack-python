# Ce code définit une fonction nommée obtenir_liste_fruits. À l'intérieur de la fonction,
# une liste appelée "fruits" est créée, contenant les chaînes de caractères "pomme", "cerise", et "orange".
# Ensuite, la fonction retourne cette liste.
def liste_de_fruits():
    fruits = ["pomme", "cerise", "orange"]
    return fruits

# Le meme principe, nous appellons la fonction puis nous affichons le nom de la fonction
liste_de_fruits = liste_de_fruits()

# Affichage du résultat
print(liste_de_fruits)
