def ajout_un_fruit():
    # Définir la liste de fruits
    fruits = ["pomme", "cerise", "orange"]

    # Ajouter "Melon" à la liste
    # Cette ligne ajoute la chaîne "Melon" à la fin de la liste en utilisant la méthode append().
    fruits.append("Melon")

    # Afficher la mise à jour de la liste
    print(fruits)

# Appeler la fonction pour ajouter "Melon" à la liste
ajout_un_fruit()
